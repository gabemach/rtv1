#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;

	i = 0;
	while (s[i] != (char)c && *s != 0)
		s++;
	if (s[i] == c)
		return ((char*)s);
	else
		return (NULL);
	return (NULL);
}
